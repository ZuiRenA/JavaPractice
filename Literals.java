public class Literals {
    public static void main(String[] args){
        int i1 = 0x2f;
        System.out.println(Integer.toBinaryString(i1));
        int i2 = 0x2F;
        System.out.println(i2);
        char c = 0xffff;
        System.out.println(c);
        byte b = 0x7f;
        System.out.println(b);
        short s = 0x7fff;
        System.out.println(s);

    }
}
